CREATE OR REPLACE PACKAGE BODY oramtssamp AS
   FUNCTION do_credit(
                       acctnum NUMBER, 
                       acctpin NUMBER, 
		               amount  REAL,
		               balance OUT NUMBER
		            ) RETURN NUMBER 
   IS
     retval NUMBER;
   BEGIN
      balance := 0;
	  retval  := 0;
      /*------ update the accounts table --------*/
      UPDATE accounts 
	    SET currbal_accounts = currbal_accounts + amount
      WHERE 
	   ( acctnum_accounts  = acctnum 
	       AND 
		 acctpin_accounts  = acctpin 
	   );
      /*------ get the new balance -------------*/
      SELECT currbal_accounts 
	     INTO balance 
      FROM accounts
      WHERE 
	   ( acctnum_accounts  = acctnum 
	       AND 
		 acctpin_accounts  = acctpin 
	   );
       RETURN retval;

   EXCEPTION
      WHEN NO_DATA_FOUND THEN
      BEGIN
         retval := SQLCODE;
         RETURN retval;
      END;

      WHEN OTHERS THEN
	  BEGIN
           retval := SQLCODE;
           RETURN retval;
	  END;
   END do_credit;

   FUNCTION do_debit(
                     acctnum NUMBER, 
                     acctpin NUMBER, 
                     amount  REAL,
                     balance OUT NUMBER
				   ) RETURN NUMBER
   IS
      insufficient_funds EXCEPTION;
      retval      NUMBER;
   BEGIN
      balance := 0;
      retval  := 0;
      /*------ update the accounts table --------*/
      UPDATE accounts 
	    SET currbal_accounts = currbal_accounts - amount
      WHERE 
	   ( 
	     (acctnum_accounts  = acctnum)
	       AND 
		 (acctpin_accounts  = acctpin)
		   AND 
         (currbal_accounts - amount) > 0
	   );
      /*------ get the new balance -------------*/
      SELECT currbal_accounts 
	    INTO balance 
	  FROM accounts
      WHERE 
	   (
 	     (acctnum_accounts  = acctnum)
	       AND 
		 (acctpin_accounts  = acctpin)
	   );
       RETURN retval;

   EXCEPTION
      WHEN NO_DATA_FOUND THEN
	  BEGIN
           retval := SQLCODE;
           RETURN retval;
	  END;
      WHEN insufficient_funds THEN
	  BEGIN
           retval := SQLCODE;
           RETURN retval;
	  END;
      WHEN OTHERS THEN
	  BEGIN
           retval := SQLCODE;
           RETURN retval;
	  END;
   END do_debit;

   FUNCTION do_balance(
                       acctnum NUMBER, 
		               acctpin NUMBER, 
		               balance OUT NUMBER
                      ) RETURN NUMBER
   IS 
      retval NUMBER;
   BEGIN   
      balance := 0;
      retval  := 0;
      SELECT currbal_accounts 
	    INTO balance 
	  FROM accounts
      WHERE 
	   (
 	     (acctnum_accounts  = acctnum)
	       AND 
		 (acctpin_accounts  = acctpin)
	   );
       RETURN retval;

   EXCEPTION
      WHEN NO_DATA_FOUND THEN
	  BEGIN
           retval := SQLCODE;
           RETURN retval;
	  END;

      WHEN OTHERS THEN
	  BEGIN
           retval := SQLCODE;
           RETURN retval;
	  END;
   END do_balance;

   FUNCTION do_transfer(
                        fracnum NUMBER, 
                        fracpin NUMBER, 
                        toacnum NUMBER, 
                        amount  REAL,
			            balance OUT NUMBER
		              ) RETURN NUMBER
   IS 
      retval NUMBER;
   BEGIN   
      balance := 0;
      retval  := 0;
      /*------ debit the from account ----------*/
      UPDATE accounts 
	    SET currbal_accounts = currbal_accounts - amount
      WHERE 
	   ( 
	     (acctnum_accounts  = fracnum)
	       AND 
		 (acctpin_accounts  = fracpin)
		   AND 
         (currbal_accounts - amount) > 0
	   );
      /*------ credit the to account -----------*/
      UPDATE accounts 
	    SET currbal_accounts = currbal_accounts + amount
      WHERE 
	     (acctnum_accounts  = toacnum);
      /*------ get the new balance -------------*/
      SELECT currbal_accounts 
	    INTO balance 
	  FROM accounts
      WHERE 
	   (
 	     (acctnum_accounts  = fracnum)
	       AND 
		 (acctpin_accounts  = fracpin)
	   );
      RETURN retval;

   EXCEPTION
      WHEN NO_DATA_FOUND THEN
	  BEGIN
        retval := SQLCODE;
        RETURN retval;
	  END;

      WHEN OTHERS THEN
	  BEGIN
        retval := SQLCODE;
        RETURN retval;
	  END;
