create or replace procedure validate_atm_txns
as
  CURSOR txn_log_cursor  IS
	SELECT  
		TXNTYPE_TXACTS, TXNACCT_TXACTS, TXNAMNT_TXACTS
	FROM txacts;
   unknown_txn_type 	EXCEPTION;
   txntype              VARCHAR2(10);
   txnacct              NUMBER;
   txnamnt              NUMBER;
begin
-- for each transaction recorded in the txn log, reconcile the
-- corresponding account
     open txn_log_cursor;
     loop
	FETCH txn_log_cursor INTO txntype, txnacct, txnamnt;
        EXIT WHEN  txn_log_cursor%NOTFOUND;
        if txntype = 'CREDIT' 
        then
           txnamnt := -txnamnt;
        elsif txntype = 'DEBIT' 
        then
           txnamnt := txnamnt;
        else
           raise unknown_txn_type;
        end if;
        UPDATE accounts SET currbal_accounts =  currbal_accounts + txnamnt;
        if txn_log_cursor%ISOPEN 
        then
          close txn_log_cursor;
        end if;
      end loop;
exception
        when unknown_txn_type then raise;
end;
